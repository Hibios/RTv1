#include "libft.h"

void	ft_free_arr(char ***mas)
{
	char	**s;

	s = *mas;
	while (*s)
	{
		if (*s)
		{
			free(*s);
			*s = NULL;
		}
		s++;
	}
	free(*mas);
}
