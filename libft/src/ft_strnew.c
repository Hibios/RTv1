#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	if ((size + 1) == 0)
		return (NULL);
	str = (char *)malloc(size + 1);
	if (str == NULL)
		return (NULL);
	ft_memset(str, (int)'\0', size + 1);
	return (str);
}
